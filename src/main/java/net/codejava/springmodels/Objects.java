package net.codejava.springmodels;

public class Objects {
	private int object_id;
	private int project_id;
	private int collected_quantity;
	private int quantity;
	private String object_name;

	public int getObject_id() {
		return object_id;
	}

	public void setObject_id(int object_id) {
		this.object_id = object_id;
	}

	public int getProject_id() {
		return project_id;
	}

	public void setProject_id(int project_id) {
		this.project_id = project_id;
	}

	public int getCollected_quantity() {
		return collected_quantity;
	}

	public void setCollected_quantity(int collected_quantity) {
		this.collected_quantity = collected_quantity;
	}

	public int getQuantity() {
		return quantity;
	}

	public void setQuantity(int quantity) {
		this.quantity = quantity;
	}

	public String getObject_name() {
		return object_name;
	}

	public void setObject_name(String object_name) {
		this.object_name = object_name;
	}

}
